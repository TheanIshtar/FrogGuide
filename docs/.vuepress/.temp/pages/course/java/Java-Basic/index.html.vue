<h2 id="_1-java-la-gi" tabindex="-1"><a class="header-anchor" href="#_1-java-la-gi" aria-hidden="true">#</a> 1. Java là gì ?</h2>
<p>Java là một ngôn ngữ lập trình có từ <code v-pre>1995</code> bởi <code v-pre>Sun Microsystems</code>, hiện thuộc sở hữu của <a href="https://vi.wikipedia.org/wiki/Oracle_Corporation" target="_blank" rel="noopener noreferrer">Oracle Corporation<ExternalLinkIcon/></a>.</p>
<blockquote>
<p>🤔 Vậy nó làm được gì, có ăn được không  ?</p>
</blockquote>
<p>==&gt; <code v-pre>Java</code> có thể làm được mọi thứ như : <code v-pre>Web</code>, <code v-pre>App</code>, <code v-pre>Server</code>, <code v-pre>Database</code>... và đặt trưng của <code v-pre>Java</code> là <code v-pre>OOP</code> (<em>Object-oriented Programing</em>)</p>
<blockquote>
<p>💭 Ngôn ngữ lập trình chỉ là công cụ, vậy nên có cái thiên về này có cái thiên về kia, và tư duy ý tưởng của chính bạn mới là cốt lõi.</p>
</blockquote>
<p>Có một số bonus thêm cho bạn :</p>
<blockquote>
<p>💡<code v-pre>Java</code> được phát triển từ <code v-pre>C</code> và <code v-pre>C++</code> là chủ yếu</p>
<p>💡 <code v-pre>Java</code> từng được Microsoft rất thích, nhưng vì vấn đề bản quyền, và Microsoft đã tạo ra <code v-pre>C#</code> có cấu trúc tương đướng với <code v-pre>Java</code></p>
<p>💡<code v-pre>Java</code> và <code v-pre>Javascript</code> hoàn toàn không giống nhau và có quan hệ nhau một tý gì.</p>
</blockquote>
