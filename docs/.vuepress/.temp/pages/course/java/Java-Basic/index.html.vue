<h2 id="_2-điem-manh-cua-java" tabindex="-1"><a class="header-anchor" href="#_2-điem-manh-cua-java" aria-hidden="true">#</a> 2. Điểm mạnh của Java</h2>
<ul>
<li>Được sử dụng nhiều nhất thế giới, khoảng <code v-pre>3 tỷ</code> thiết bị.</li>
